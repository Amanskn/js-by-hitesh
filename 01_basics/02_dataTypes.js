// There are two category of datatypes in JS
// 1. Primitive datatype
// 2. Non-Primitive datatype

// ********** Primitive datatypes are as follows:-
// string       ====> typeof "Aman" is string
// boolean      ====> typeof true is boolean
// number       ====> typeof 23 is number
// undefined    ====> typeof undefined is undefined
// null         ====> typeof null is object
// bigint       ====> typeof 23n is bigint
// symbol       ====> typeof Symbol(23) is symbol

// *********** Non -primitive datatypes are as follows:-
// 1. object    ====> typeof {} is object                    
// 2. array     ====> typeof [] is object
// 3. function  ====> typeof function fun(){} is function